# SLICING
players = ['sr7', 'messi', 'travis', 'chicha',  'corona']
print(players[0:3])

# SLICE es trabajar con un grupo espesifico 
# de elementos de una lista 
print(players[1:3])  #['messi', 'travis', 'chicha']
print(players[:4])   #['cr7', messi, 'travis, 'chicha']
print(players[2:])   #

print(players[-3:])
print(players[-3:-1])
print(players[4:2])


